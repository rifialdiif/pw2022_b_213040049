<?php
// pengulangan
// for
// while
// do.. while
// foreach : pengulangan khusus array

// for
for( $i = 0; $i < 5; $i++ ) { 
    echo "Hello World! <br>";
}
echo "<hr>";

// while
$i = 0;
while( $i < 5 ) {
    echo "Hello World! <br>";
$i++;    
}
echo "<hr>";

// do.. while
$i = 0;
do {
    echo "Hello World! <br>";
    $i++; 
} while( $i < 5 );
echo "<hr>";
?>